package org.example.query.interfaces;

/**
 * The {@code IQueryView} interface defines a contract for classes that represent
 * query views in a user interface.
 *
 * @author Group1
 * @version 1.0
 */
public interface IQueryView {

    /**
     * Displays the query view to the user.
     * This method should be implemented to show the query view
     * within the user interface.
     */
    public void display();
}
