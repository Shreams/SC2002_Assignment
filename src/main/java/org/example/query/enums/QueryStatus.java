package org.example.query.enums;

/**
 * This enum represents the status used by enquiry and suggestion.
 *
 * @author Group1
 * @version 1.0
 */
public enum QueryStatus {
    /**
     * The query is pending approval.
     */
    PENDING,
    
    /**
     * The query is  approved.
     */
    APPROVED,
    
    /**
     * The query is denied.
     */
    DENIED
}
