package org.example.login.interfaces;
/**
 * The {@code ILoginDisplay} interface defines a contract for classes that provide display functionality for login-related views.
 * Implementing classes should provide a method to display relevant information to the user.
 * This interface is part of the {@code org.example.login.interfaces} package.
 *
 * @author Group1
 * @version 1.0
 */
public interface ILoginDisplay {
	/**
     * Displays relevant information for the login view.
     */
    public void display();
}
